package exercise.chapter_13;

public class IntegerRange {
    public static void main(String[] args) {
        // Byte 범위 : -128 ~ 127
        // Short 범위 : -32768 ~ 32767
        short myShort = 130;
        System.out.println(myShort);
        long myInt = -2389328758397485L;
        System.out.println(myInt);
    }
}
