package exercise.chapter_18;

public class Operator {
    public static void main(String[] args) {

        // += *=
        int myInt = 5;
        myInt +=1;
        System.out.println(myInt); // 6

        // 단항 연산자 ++
        int myInt2 = 10;
        myInt2++;
        System.out.println(myInt2); // 1
    }
}
